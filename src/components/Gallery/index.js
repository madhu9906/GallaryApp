class Gallery extends Component {
  state = {activeImageId: imagesList[0].id}

  imageChange = id => {
    this.setState({activeImageId: id})
  }

  getFilteredImages = () => {
    const filteredImages = imagesList.filter(eachItem => eachItem)
    return filteredImages
  }

  render() {
    const {activeImageId} = this.state
    const filteredImages = this.getFilteredImages()

    return (
      <div className="bg-container">
        <ul>
          <li className="list-item">
            <img
              src={filteredImages[activeImageId].imageUrl}
              alt={filteredImages[activeImageId].imageAltText}
            />
          </li>
        </ul>
        <div className="bottom-container">
          <h1>Nature Photography</h1>
          <p>Nature Photography by Rahul</p>
          <ul className="thumbnail-container">
            {imagesList.map(eachItem => (
              <ThumbnailItem
                imagesList={eachItem}
                key={eachItem.id}
                imageChange={this.imageChange}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
